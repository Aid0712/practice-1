"""Простые коллекции: список, словарь, множество."""

# ВАЖНО: список хранит порядок
tasks = ["учиться", "делать зарядку", "читать"]
print("Первая задача:", tasks[0])

# ВАЖНО: словарь хранит пары ключ-значение
user = {"name": "Аня", "age": 16}
print("Имя:", user["name"])

# ВАЖНО: множество хранит только уникальные значения
unique_numbers = {1, 2, 2, 3}
print("Уникальные числа:", unique_numbers)
